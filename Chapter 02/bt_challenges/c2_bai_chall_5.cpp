#include <iostream>

using namespace std;

int main() {
    // Declare variables to store values
    double value1 = 28.0;
    double value2 = 32.0;
    double value3 = 37.0;
    double value4 = 24.0;
    double value5 = 33.0;

    // Calculate the sum of the values
    double sum = value1 + value2 + value3 + value4 + value5;

    // Calculate the average
    double average = sum / 5.0;

    // Display the average
    cout << "The average of the values is: " << average << endl;

    return 0;
}
